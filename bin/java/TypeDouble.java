public class TypeDouble {
    public static void main(String args[]) {
        double a = 1.123123;
        double b = 14.2123;
        double c = 234.53;
        double d = 4421.1283;

        System.out.println(a+b);
        System.out.println(c-d);
        System.out.println(c*a);
        System.out.println(d/b);
    }
}
