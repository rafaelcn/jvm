public class Matrices {
    public static void main(String args[]) {
        int a[][] = new int[5][3];

        a[0][0] = 3;
        a[1][0] = 7;
        a[1][2] = 2;

        System.out.print("A[0][0]: " + a[0][0] + ", A[1][0]: " + a[1][0]);
        System.out.print("A[1][2]: " + a[1][2]);
    }
}
