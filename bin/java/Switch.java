public class Switch {
    public static void main(String args[]) {
        Integer a = 3;

        switch(a) {
            case 1:
                System.out.println("これはいちです。");
            break;
            case 2:
                System.out.println("これわにです。");
            break;
            case 3:
                System.out.println("これはさんです。");
            break;
            case 4:
                System.out.println("これはよんです。");
            break;
            default:
        }
    }
}